package Class7;

public class E7WhileLoop {

    public static void main(String[] args) {
        // print these number in one line 3 5 7 9 11 13 with while loop

        int counter=3;
        while (counter<=13){  // or <14
            System.out.print(counter+" "); // to print in a straight line remove ln from print {println "ln mean new line")
            counter+=2;
        }
    }
}
