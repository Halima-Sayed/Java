package Class3;

public class E4Division {
    public static void main(String[] args) {

        System.out.println(10/5);
        // if the data type is int we will not see the decimal
        System.out.println(11/5);
        // whenever the data types will be double or float we will see the decimal part in answer
        System.out.println(11.0/5.0);
        int number1=10;
        int number2=4;
        System.out.println(10/4);
        double num1=10;
        double num2=4;
        System.out.println(num1/num2);
        // All the information how an operation should be performed are based on DataTypes

    }
}
