public class E14TypeCasting {
    public static void main(String[] args) {

        short num=1200;
        byte num2=(byte)num;
        System.out.println(num2); //will get unexpected results

        short num3=126;
        byte num4=(byte)num3;
        System.out.println(num4); // will give us 126






    }
}
