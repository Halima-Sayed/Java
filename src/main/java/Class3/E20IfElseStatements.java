package Class3;

public class E20IfElseStatements {
    public static void main(String[] args) {

        /*
        I want to write a program which checks my account balance
        if account is greater than 2000 it should say lets go shopping
        otherwise it should say you need to save more
         */
        int accountBalance=2000;
        if(accountBalance>2000){
            System.out.println("Lets go shopping");
        } else{
            System.out.println("You need to save more");
        }
    }
}
