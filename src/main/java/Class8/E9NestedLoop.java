package Class8;

public class E9NestedLoop {
    public static void main(String[] args) {

        for (int  i= 0;  i<100 ; i++) {
            for (int j = 0; j < 5; j++) { // the number 5 is gonna give us 5 * if we put 3 it will print out 3 etc
                System.out.print("* ");

            }

            System.out.println();
        }
    }
}
