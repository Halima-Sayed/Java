package Class7;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number=0;
        // Below we are using the increment operator and it increases the value by one
        number++; // zero + 1 = 1
        number++; // 1 + 1= 2
        System.out.println(number);

        int num2=10;
        num2--; // 10 - 1 = 9
        System.out.println(num2);

    }
}
